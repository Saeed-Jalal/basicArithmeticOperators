public class basicArithmeticOperators {
    /* Basic arithmetic operators are : +, -, /, %
    % is for Modulo and it returns remainder.
     */
    public static void main(String args[]){
        int num1 = 510;
        int num2 = 310;


        System.out.println("num1 + num2:" +(num1+num2));
        System.out.println("num1-num2:" +(num1-num2));
        System.out.println("num1/num2:" +(num1/num2));
        System.out.println("num1%num2:" +(num1%num2));
    }
}
